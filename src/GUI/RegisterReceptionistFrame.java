  public boolean validateInput(){
      char [] pw=pwd.getPassword();
       char [] repw=repwd.getPassword();
      return !(txtUserId.getText().isEmpty()||pw.length<4||repw.length<4);
      
  }
    public boolean passwordMatch(String a,String b)
    {
        return a.equals(b);
    }    
