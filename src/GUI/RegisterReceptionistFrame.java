    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
if(validateInput()==false){
    JOptionPane.showMessageDialog(null,"Please input id all the details!");
            return;
}      
        
        try{
            String id=jcEmpId.getSelectedItem().toString();
            String userid=txtUserId.getText();
           
             char [] pw=pwd.getPassword();
             String pass=String.valueOf(pw);
       char [] repw=repwd.getPassword();
                 String repass=String.valueOf(repw);
          if(passwordMatch(pass,repass)==false){
    JOptionPane.showMessageDialog(null,"Password Does not match with retype password!");
            return;
}   
                 UserDetails user =new UserDetails();
                 user.setUserId(userid);
                 System.out.println("");
                 user.setUserName(txtEmpName.getText());
                 user.setEmpId(id);
                 user.setPassword(pass);
                 user.setUsertype("RECEPTIONIST");
                boolean addR= ReceptionistDao.addReceptionist(user);
                 if(addR){
                      JOptionPane.showMessageDialog(null,"Record added successfully");
                      clearText();
                      loadReceptionistDetails();
                 }else   JOptionPane.showMessageDialog(null,"Could not add Receptionist to the DB");
        }catch (SQLException ex) {
        JOptionPane.showMessageDialog(null,"SQL query Exception");
        ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

